package L5_IterativeLoops;

class IterativeDemo {
    public static void main(String[] args) {
        System.out.println("Iterative Demo | Loops");

        // Printing Msg 20 times using LOOP
        int i=0;
        int n=20;
        while(i<=n){
            System.out.println(" I love Java");
            i+=1;
        }
    }
}
