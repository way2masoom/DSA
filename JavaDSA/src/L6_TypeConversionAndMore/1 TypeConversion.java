package L6_TypeConversionAndMore;

class TypeConversion {
    public static void main(String[] args) {
        System.out.println("Type Casting");

        // Implicit Conversion
        float myFloat=5;
        System.out.println(myFloat);

        // Explicit conversion
        int myInt=(int)5.0f;
        System.out.println(myInt);
    }
}
