package L3_Operators;

public class RelationalOperators {
    public static void main(String[] args) {
        System.out.println("Relational Operators");
        // Relational operator are used to check relation between two operates.
        // Relational operator will return True or false

        int a=8, b=20;

        System.out.println(a +" " +b);

        System.out.println(a==b); // Equal to operator
        System.out.println(a!=b); // Not Equal to operator

        System.out.println(a>b); // false
        System.out.println(a<b); // True
        System.out.println(a<=b); // True
        System.out.println(a>=b); // False
    }
}
